function update(){
    drawRect(0,0, canvasWidth, canvasHeight, 'black');
    drawRect(canvasWidth/2, canvasHeight/2, 10, 15, 'white');
    for(var y = 0; y < mapHeight; ++y)
	{
		for(var x = 0; x < mapWidth; ++x)
		{
            switch(map[((y*mapWidth)+x)])
			{
				case 0:
					drawRect(x*tileWidth, y*tileHeight, tileWidth, tileHeight, 'black');
					break;
				case 1:
                    drawRect(x*tileWidth, y*tileHeight, tileWidth, tileHeight, 'white');
                    break;
			}

        }
	}

}

function keyDown(evt) {
    switch(evt.keyCode) {
        case 37:
            holdLeft=true;
            break;
        case 38:
            if(onG) {
                yv=-10;
            }
            break;
        case 39:
            holdRight=true;
            break;
    }
}
function keyUp(evt) {
    switch(evt.keyCode) {
        case 37:
            holdLeft=false;
            break;
        case 38:
            if(yv<-3) {
                yv=-3;
            }
            break;
        case 39:
            holdRight=false;
            break;
    }
}

function drawRect(startX, startY, width, height, color ) {
    canvasContext.fillStyle = color;
    canvasContext.fillRect(startX, startY, width, height);
}